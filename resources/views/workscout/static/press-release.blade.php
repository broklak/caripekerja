                </p>
                <p>
                    Saat ini CariPekerja.com memiliki 16 kategori pekerjaan, yaitu Pelayan Restoran, Penjaga Toko, Admin, Sopir, Buruh Pabrik, Satpam, Montir, Kurir, Juru Masak, Kuli Bangunan, Office Boy, Tukang Kayu, Penjahit, Pemayet, Asisten Rumah Tangga, Baby Sitter.
                </p>
                <p>
                    Untuk mencari Pekerja yang Anda inginkan, Anda dapat memilih pekerja dengan 3 langkah mudah: masukan profesi pekerja,  pilih kriteria pekerja yang anda inginkan, dan dapatkan kontak pekerja untuk kemudian anda rekrut. Dalam hitungan menit, anda dapat menemukan pekerja yang cocok dengan anda. Anda juga dapat membuat lowongan kerja secara gratis di CariPekerja.com. Anda hanya akan dikenakan biaya apabila Anda sudah tertarik dengan profil Pekerja yang anda inginkan.
                </p>
