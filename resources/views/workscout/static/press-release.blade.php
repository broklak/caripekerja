                    Tahukah Anda, Indonesia merupakan negara ke-3 di ASEAN dengan tingkat pengangguran tertinggi sebesar 5.5%. Padahal 90% tenaga kerja di Indonesia merupakan lulusan Non-Sarjana dan bekerja sebagai pekerja kerah biru, dan 99.9% Badan Usaha di Indonesia merupakan Usaha Kecil dan Menengah. Namun, mengapa angka pengangguran di Indonesia relative tinggi?
